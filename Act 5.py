a=int(input("Enter the temperature:"))
if a==50:
    print("Its summer season so you have to wear light clothes")
elif a==40:
    print("Its spring season so you can wear whatever clothes you want")
elif a==30:
    print("Its fall so you have to wear proper clothes")
elif a==20:
    print("Its rainy season so you have to wear slightly warm clothes")
else:
    print("Its winter season so you have to wear really warm clothes")